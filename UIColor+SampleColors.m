#import "UIColor+SampleColors.h"

@implementation UIColor (SampleColors)

+(UIColor *) SampleGray {
    return [UIColor colorWithRed:0.3 green:0.3 blue:0.3 alpha:0.7];
}
+(UIColor *) SampleGrayLight {
    
    return [UIColor colorWithRed:0.6 green:0.6 blue:0.6 alpha:0.2];
}


@end
